package Objetuak;

public class Pelikulak {

    int IDPelikula;
    String tituloa;
    String generoa;
    int irabaziak;
    int aurrekontua;
    String trailera;
    String irudia;
    int idEstrenaldia;
    int idZuzendaria;


    public Pelikulak(int IDPelikula, String tituloa, String generoa, int irabaziak, int aurrekontua, String trailera, String irudia, int idEstrenaldia, int idZuzendaria) {
        this.IDPelikula = IDPelikula;
        this.tituloa = tituloa;
        this.generoa = generoa;
        this.irabaziak = irabaziak;
        this.aurrekontua = aurrekontua;
        this.trailera = trailera;
        this.irudia = irudia;
        this.idEstrenaldia = idEstrenaldia;
        this.idZuzendaria = idZuzendaria;
    }
}
